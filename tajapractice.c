void scanword() { //단어 화면에 입력받게 해주는 함수

	int p = 0;

	while (p < wordtime) {
		
		p++;

		i = 0;

		int scanc;

		endTime = clock();

		gap = (float)(endTime - startTime) / (CLOCKS_PER_SEC);

		if (gap > 30) { //게임지속시간 30s

			system("cls");

			design();
			SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), 2);
			gotoxy(40, 11);

			printf("---게 임 종 료---");

			Sleep(2000);

			main();

		}



		if (_kbhit()) {

			scanc = getch();

			if (scanc != 8 && scanc != 13) {

				gotoxy(10, 25);

				scan[sword++] = scanc;

				printf("                 ");

				gotoxy(10, 25);

				SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), 15);

				printf("%s", scan);

			}

			else if (scanc == 8) {

				gotoxy(1, 25);

				gotoxy(5, 25);

				scan[--sword] = scanc;

				if (sword == -1)

					sword = 0;

				printf("%s", scan);



			}

			else if (scanc == 13) {

				sword = 0;

				gotoxy(1, 25);

				for (i = 0; i <= wordmax; i++)

				{

					if (!strcmp(scan, word[i]))

					{



						x2 = remem[i][0];

						y2 = remem[i][1];

						f2 = remem[i][2];



						gotoxy(x2, y2);

						printf("          ");

						gotoxy(1, 25);

						switch (f2) {

						case 1: score1 += 150; break;

						case 2: score1 += 200; break;

						case 3: score1 += 250; break;

						case 4: score1 += 300; break;

						default: break;

						}

						gotoxy(6, 5);

						SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), 15);

						printf("현재점수 :%d", score1);





					}

				}

				for (i = 0; i < 50; i++) {

					scan[i] = NULL;

				}

			}

		}

	}

}

